// SERVICE DETAILS TOGGLE
// ============================================

function toggleServiceDetails(serviceId) {
    const details = document.getElementById(`service-details-${serviceId}`);
    const button = document.querySelector(`[data-service="${serviceId}"] .service-toggle-btn`);
    
    if (details && button) {
        const isActive = details.classList.contains('active');
        
        if (isActive) {
            details.classList.remove('active');
            button.classList.remove('active');
            button.querySelector('.btn-text').textContent = 'View Details';
        } else {
            details.classList.add('active');
            button.classList.add('active');
            button.querySelector('.btn-text').textContent = 'Hide Details';
        }
    }
}

